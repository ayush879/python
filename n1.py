import numpy as np
a=np.array([1,2,3])
print(a)
print(type(a))
a=np.array([1,2,3],dtype=complex)
print(a)
print(type(a))
a=np.asanyarray([1,2,3])
print(a)
print(type(a))
a=np.arange(1,10,2)
print(a)
print(type(a))
a=np.ones(3)
print(a)
print(type(a))
l=[(1,2,3,4),(1,2,3,4)]
a=np.ones_like(l)
print(a)
l=[(1,2,3,4),(1,2,3,4)]
a=np.zeros_like(l)
print(a)
print(type(a))
a=np.empty(2)
print(a)
print(type(a))
a=np.empty([2,2])
print(a)
print(type(a))
l=[(1,2,3,4),(1,2,3,4)]
a=np.empty_like(l)
print(a)
print(type(a))
a=np.full(2,10)
print(a)
print(type(a))
l=[(1,2,3,4),(1,2,3,4)]
a=np.full_like(l,10)
print(a)
print(type(a))
a=np.eye(2)
print(a)
print(type(a))
a=np.identity(2)
print(a)
print(type(a))


